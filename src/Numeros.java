public class Numeros {
    int cont, valor;
    public Numeros (int valor, int cont){
        this.valor = valor;
        this.cont = cont;
    }

    public int getValor(){
        return valor;
    }

    public void setValor(int valor){
        this.valor = valor;
    }

    public int getCont(){
        return cont;
    }
}
